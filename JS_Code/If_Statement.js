//If Statment

var x = 10;
if (x > 5) {
    console.log("x is greater than 5");
}
else {
    console.log("x is less than 5");
}

//If else if else

let y = 4;
if (y > 5) {
    console.log("y is greater than 5");
}
else if (y < 5) {
    console.log("y is less than 5");
}
else {
    console.log("y is equal to 5");
}