// Type Coercion: It is the process of converting value from one type to another (such as string to number, object to boolean, and so on). Any type, be it primitive or an object, is a valid subject for type coercion.

//Concatenation
let a = 45;
let b = "34";

console.log(a + b); //output: 4534


//Addition
let c = 45;
let d = 34;

console.log(c + d); //output: 79